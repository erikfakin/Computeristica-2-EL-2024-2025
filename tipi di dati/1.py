# Tipi di dati

#int - integer o numero intero
a = 5

print(type(a))


#float - floating point number, numero decimale
b = 3.14

print(type(b))

#str - stringa o testo
c = 'testo'
print(type(c))

#bool - boolean, booleano
d = True
print(type(d))


#list - collezione di dati
e = [1,2,3,"Giallo", True, 3.14]

print(type(e))

#dict - ditionary o dizionari
f = {
        "nome":"Marco",
        "eta": 13
    }
print(type(f))


#tuple -
g = (1, 3, "blue")

print(type(g))








